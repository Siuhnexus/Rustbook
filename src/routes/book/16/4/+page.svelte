   Extending Cargo with Custom Commands
  </a>
 </h2>
 <p>
  Cargo is designed so you can extend it with new subcommands without having to
modify Cargo. If a binary in your
  <code>
   $PATH
  </code>
  is named
  <code>
   cargo-something
  </code>
  , you can
run it as if it was a Cargo subcommand by running
  <code>
   cargo something
  </code>
  . Custom
commands like this are also listed when you run
  <code>
   cargo --list
  </code>
  . Being able to
use
  <code>
   cargo install
  </code>
  to install extensions and then run them just like the
built-in Cargo tools is a super convenient benefit of Cargo’s design!
 </p>
